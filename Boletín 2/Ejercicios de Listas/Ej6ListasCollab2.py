lista = [1, 2, 3, 4, 5]
print("La lista es: ", lista)
print("El mayor valor de la lista es: ", max(lista))
print("El menor valor de la lista es: ", min(lista))
print("La diferencia entre el mayor y el menor valor de la lista es: ", max(lista) - min(lista))