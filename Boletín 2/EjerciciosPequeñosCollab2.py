# Se cree comúnmente que 1 año humano equivale a 7 años en un perro. Construye un programa que le pregunte al usuario en qué año nació y le informe de su edad perruna. Asume que el año actual es 2021.
c = int(input("Introduce el año en que naciste: "))
d = (2021 - c) * 7
print("Tu edad perruna es: ", d)